package Number_Programs;
import java.util.Scanner;

public class DemoPrint {

	public static void main(String[] args) {
		Scanner sc= new Scanner(System.in);
		int n=sc.nextInt();
		
		while(n>0)
		{
			System.out.println(n);
			n--;
			
		}
		
		
		
		System.out.println();
		
		int x=5;
		int y=x++;
		System.out.println(y);
		
		
		
		
	}
}
